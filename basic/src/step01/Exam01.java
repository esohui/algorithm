package step01;

/* 화면 출력 문제 */
public class Exam01 {
	public static void main(String[] args) {
		
		// 일반 출력
        System.out.println("Hello World!");
        System.out.println();
        
        // 고양이 출력
        System.out.println("\\    /\\");
        System.out.println(" )  ( ')");
        System.out.println("(  /  )");
        System.out.println(" \\(__)|");
        System.out.println();
        
        // 강아지 출력
        System.out.println("|\\_/|");
        System.out.println("|q p|   /}");
        System.out.println("( 0 )\"\"\"\\");
        System.out.println("|\"^\"`    |");
        System.out.println("||_/=\\\\__|");
        System.out.println();
        
    }
}
